class Calculator:
  def __init__(self, var01, var02):
    self.var01 = var01
    self.var02 = var02

  def plus(self):
    print(self.var01 + self.var02)

  def minus(self):
    print(self.var01 - self.var02)

  def multiply(self):
    print(self.var01 * self.var02)

  def divide(self):
    print(self.var01 / self.var02)